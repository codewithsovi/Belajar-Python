# konversi integer ke float
print(float(5))

# konversi float ke integer
print(int(5.0))
print(int(-5.6))

# konversi string
print(int('35'))
print(str(25))
print(float("25"))
print(str(25.6))

# konversi kumpulan data
# 1. list >> set
print(set([1,2,3]))

# 2. set >> tuple
print(tuple({5, 6, 7}))

# 3. tuple >> list
print(list('hello'))

# 4. list >> dictionary
print(dict([[1,2], [3,4]]))

# 5. tuple >> dictionary
print(dict[(3, 36), (9,5)])

